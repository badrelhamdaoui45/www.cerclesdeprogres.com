 <content:encoded>&lt;div class='rss_texte'&gt;&lt;p&gt; &lt;span style=&#034;color:#4F7A7F&#034;&gt;&lt;strong&gt;L'association Cercles de Progr&#232;s France est organisme de formation depuis 2012, r&#233;f&#233;renc&#233;e DataDock depuis 2018, et certifi&#233;e Qualiopi depuis Octobre 2021.&lt;/strong&gt;&lt;/span&gt;&lt;/p&gt;
&lt;p&gt; &lt;span style=&#034;color:#4F7A7F&#034;&gt;&lt;span style=&#034;color:#4F7A7F&#034;&gt;&lt;strong&gt;Le degr&#233; de responsabilit&#233; professionnelle&lt;/strong&gt; est la premi&#232;re des conditions d'adh&#233;sion&lt;/span&gt;&lt;/span&gt;&lt;/p&gt;
&lt;p&gt; &lt;span style=&#034;color:#4F7A7F&#034;&gt;&#171; &lt;i&gt;Les membres des Cercles sont les N&#176;1 de leur entreprise ou de leur institution. A ce titre, ils exercent la fonction de Direction G&#233;n&#233;rale, quelle que soit la taille de leur organisme, &#224; l'exception d'entreprise unipersonnelle, (professions lib&#233;rales, ou consultants), qui sont limit&#233;es &#224; 2 par Club. Pour les tr&#232;s grandes entreprises, les dirigeants de filiales ou d'unit&#233;s r&#233;gionales peuvent &#234;tre accept&#233;s. L'adh&#233;sion est intuitu personae.&lt;/i&gt; &#187;&lt;/span&gt;&lt;br /&gt; &lt;br /&gt; Les objectifs de la formation pour les dirigeants sur une ann&#233;e peuvent &#234;tre divers :&lt;/p&gt;
&lt;p&gt; * Approfondir leur r&#244;le de leader et d'excellence manag&#233;riale&lt;/p&gt;
&lt;p&gt; * Diriger en capitalisant sur l'humain, savoir donner du sens et bien communiquer&lt;/p&gt;
&lt;p&gt; * Appr&#233;hender diff&#233;rentes techniques d'entreprises&lt;/p&gt;
&lt;p&gt; * R&#233;ussir avec les enjeux de la transformation num&#233;rique et digitale&lt;/p&gt;
&lt;p&gt; * D&#233;velopper la connaissance de soi et favoriser la coh&#233;sion de ses &#233;quipes -&lt;/p&gt;
&lt;p&gt; * D&#233;velopper ses comp&#233;tences strat&#233;giques par un processus d'innovation et de cr&#233;ativit&#233;&lt;/p&gt;
&lt;p&gt; La formation est donc vari&#233;e mais tr&#232;s pragmatique avec des apports concrets et des outils &#224; mettre en place &#224; l'issue de chaque s&#233;ance. La formation a lieu sur 10 journ&#233;es, une s&#233;ance par mois (sauf Juillet et Ao&#251;t) avec un expert tous les mois, qui intervient sur le th&#232;me retenu par le Club, et propos&#233; en amont par l'animatrice lors d'une r&#233;union de programmation.&lt;/p&gt;
&lt;p&gt; Les dirigeants se doivent d'&#234;tre pr&#233;sents, actifs, participatifs, ouverts d'esprit, avec l'envie d'apprendre et faire partager ses connaissances entre pairs.&lt;/p&gt;
&lt;p&gt; Le tarif varie en fonction de la taille et la structure de l'entreprise, 3 tarifs ont &#233;t&#233; valid&#233; par le Conseil d'Administration.&lt;/p&gt;
&lt;ul&gt; &lt;li&gt; Adh&#233;sion pleine : 4.900 euros (entreprise de plus de 5 salari&#233;s)&lt;/li&gt; &lt;li&gt; Adh&#233;sion pr&#233;f&#233;rentielle : 3.000 euros (de 2 &#224; 5 salari&#233;s)&lt;/li&gt; &lt;li&gt; Adh&#233;sion demi tarif : 2.450 euros (ind&#233;pendant, retrait&#233;)&lt;/li&gt;
&lt;/ul&gt;
&lt;p&gt; Pour plus de renseignements vous pouvez contacter l'animatrice du Club de Toulouse &lt;br /&gt;au 06 89 14 26 80, Ariane de Charentenay.&lt;/p&gt;
&lt;p&gt; Chaque expert a ses propres modalit&#233;s p&#233;dagogiques pour mener &#224; bien la prestation. Tests ou questionnaires en amont, ateliers sur place, supports et exercices.&lt;/p&gt;
&lt;p&gt; Un &lt;strong&gt;annuaire des intervenants et experts agr&#233;&#233;s &lt;/strong&gt;par les Cercles de Progr&#232;s est aliment&#233; r&#233;guli&#232;rement sur la base des &#233;valuations.&lt;/p&gt;
&lt;p&gt; &lt;br /&gt; &lt;strong&gt;L'int&#233;gration d'un nouveau membre &lt;/strong&gt;est soumise &#224; une proc&#233;dure de cooptation apr&#232;s avoir &#233;t&#233; invit&#233; &#224; assister &#224; une s&#233;ance du club par un membre et s'&#234;tre entretenu avec le Pr&#233;sident. Ses pairs choisissent de l'int&#233;grer au club &#224; l'unanimit&#233; (un droit de v&#233;to peut &#234;tre exerc&#233; par l'un des membres et n'a pas &#224; &#234;tre justifi&#233;).&lt;br /&gt; Son engagement est officialis&#233; par la signature de la charte et le paiement de la cotisation annuelle. Le d&#233;lai peut &#234;tre tr&#232;s rapide pour d&#233;buter la formation.&lt;/p&gt;
&lt;p&gt; Les journ&#233;es ont lieu &#224; l'h&#244;tel Mercure St Georges, dans le centre de Toulouse, l'acc&#232;s est toujours indiqu&#233; dans l'invitation transmise aux dirigeants et &#224; leurs invit&#233;s. Les personnes en situation de handicap sont invit&#233;es &#224; nous contacter pour que nous puissions envisager ensemble toutes solutions.&lt;/p&gt;
